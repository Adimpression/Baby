      &quot;Curiousity killed the cat&quot;
    </p>
    <p>
      Don't expect your user to be curious.
    </p>
    <p>
      Curiousity will kill th cat. If you mandate that the cat be curious, it will die.
    </p>
    <p>
      Given a GUI interface, make sure the user &quot;gets it&quot;.
    </p>
    <p>
      If the interface assumes curiosity from the user to explain something you provide, it kills the cat.
    </p>
    <p>
      Cat dead is a failure point.
    </p>
    <p>
      It is most often in the scale of &quot;complete product failure&quot; since we are very social in nature.
    </p>
    <p>
      Never let the cat be curious.
    </p>
    <p>
      Never let it die.
    </p>
    <p>
      Good luck.
    </p>
    <p>
